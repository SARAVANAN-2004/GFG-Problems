class Solution {
    static Node segregate(Node head) {
        // code here
        int z = 0, o = 0, t = 0;
        Node ptr = head;
        while(ptr != null){
            if(ptr.data == 0){
                z++;
            }else if(ptr.data == 1){
                o++;
            }else{
                t++;
            }
            ptr = ptr.next;
        }
        
        ptr = head;
        while(z > 0){
            ptr.data = 0;
            ptr = ptr.next;
            z--;
        }
        
        while(o > 0){
            ptr.data =1;
            ptr = ptr.next;
            o--;
        }
        while(t > 0){
            ptr.data = 2;
            ptr = ptr.next;
            t--;
        }
        return head;
    }
}



